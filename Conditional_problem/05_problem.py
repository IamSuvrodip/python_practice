# 5. Weather Activity Suggestion 

weather = input("What the Weather Today Sunny/Rainy/Snowy: ").lower()

if weather == 'sunny':
    print("Go for a Walk")
elif weather == 'rainy':
    print("Read a Book")
elif weather == 'snowy':
    print("Build a Snowman")
else:
    print("You enter wrong Details")

